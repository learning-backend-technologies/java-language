package demo._001_variables;

public class _004_NumericSystems {

    public static void main(String[] args) {
    	
    	int numeroDecimal = 30;
    	System.out.println("Decimal number = " + numeroDecimal);

    	int numeroBinario = 0b11110;
    	System.out.println("Binary number = " + numeroBinario);

    	int numeroOctal = 036;
    	System.out.println("Octal number = " + numeroOctal);

    	int numeroHex = 0x8f6;
    	System.out.println("Hexadecimal number = " + numeroHex);
    	
    }
}
